export function searchArtists(query: string) {
  const normalizedQuery = query.trim().toLowerCase();

  if (!normalizedQuery) {
    return [];
  }

  return artistUniverse
    .filter((artist) => {
      const searchableText = [
        artist.nameKo,
        artist.nameEn,
        artist.ticker,
        artist.agency,
        artist.fandomName ?? '',
        ...artist.members,
        ...artist.keywords,
      ]
        .join(' ')
        .toLowerCase();

      return searchableText.includes(normalizedQuery);
    })
    .slice(0, 8);
}

export function getArtistV3ById(artistId: string) {
  return artistUniverse.find((artist) => artist.id === artistId);
}
